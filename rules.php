    В случае ожидания в [[шанпон|ожидание в два пона, состоящее из двух пар, каждая их которых может стать коцу]] при победе по рон, полученное коцу считается <strong>открытым</strong> и миниочки для него считаются соответственно.
    В том же случае, но при победе по цумо, полученное коцу считается <strong>закрытым</strong>.
    После подсчета миниочки фу округляются вверх до десятков. После этого требуется посмотреть в таблицу выплат, где в зависимости от количества хан и фу определить требуемые выплаты.
</div>
<div class="paragraph">
    Существуют два особых яку, которые не состоят из четырех сетов и пары:
    <ul>
        <li><strong>Кокушимусо</strong> - комбинация из 13ти тайлов, по одной единице и девятке каждой масти, по одному ветру и дракону + пара к любому из 13ти тайлов. Кокушимусо стоит <strong>якуман</strong>.</li>
        <li><strong>Чиитойцу</strong> - комбинация из семи пар любых тайлов. Чиитойцу стоит 2 хан и имеет <strong>фиксированное количество фу, равное 25</strong>.</li>
    </ul>
