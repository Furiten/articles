<h2>Нарушения правил</h2>
<div class="paragraph">
    Ну и наконец ложка дегтя: существуют определенные штрафы за нарушения правил, независимо от того, было ли нарушение преднамеренным или случайным.
    <ul>
        <li><strong>Чомбо</strong> - штраф стоимостью в манган, выплачиваемый игроком за грубое нарушение правил, не позволяющее продолжить сдачу. При чомбо дилер остается на месте, риичи депозиты возвращаются,
            хонба не добавляется, т.е. ситуация должна вернуться на состояние в начале сдачи.</li>
        <li><strong>Мертвая рука</strong> - состояние игрока, при котором он не имеет права делать никаких объявлений, при ничьей он считается нотен. Если игрок с мертвой рукой
            объявляет пон, чи, кан или победу, то он наказывается чомбо.</li>
    </ul>

    Следующие действия наказываются чомбо:
    <ul>
        <li><strong>[[Нотен|в случае отсутствия ожидающей руки (темпая)]] риичи</strong>, назначается в случае ничьей.</li>
        <li><strong>Ошибочное объявление победы</strong>, применяется в случае объявления победы и полного открытия не имеющей выигрыша руки. Ошибочное объявление победы имеет больший приоритет, чем объявления открытых сетов,
            но меньший, чем правильное объявление победы, т.е. если на один и тот же тайл было сделано два объявления победы, и одно из них было ошибочным, засчитывается второе объявление и к первому штраф чомбо <strong>не применяется</strong>.</li>
        <li><strong>Объявление кана, меняющего ожидание при риичи</strong>, назначается в случае ничьей или победы игрока объявившего такой кан.</li>
        <li>Если по вине игрока текущую раздачу <strong>невозможно продолжать</strong>.</li>
    </ul>
    Следующие ситуации караются мертвой рукой:
    <ul>
        <li>Взятие тайла из стены вне очереди, пропуск хода или неправильное взятие.</li>
        <li>Ошибочное объявление чи, пона, кана (в этом случае взятый тайл возвращается обратно в дискард), либо объявление чи не в свой ход.</li>
        <li>Неправильное количество тайлов в руке (более или менее 13 тайлов + 1 за каждый объявленный кан).</li>
        <li>Подсматривание тайлов из мертвой стены или рук других игроков.</li>
    </ul>
