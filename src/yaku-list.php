<section id="chi">
<h3>Яку на последовательностях</h3>
    <hr>
    <div class="paragraph">
        <strong>Пин-фу</strong> ([[平和|ぴんふ]]) - рука без миниочков (фу), в каком-то смысле антагонизм тойтоя. Рука должна состоять только из [[шунцу|три последовательных тайла масти, например 2-3-4]],
        не содержать якухайных пар и иметь ожидание [[рянмен|двустороннее ожидание в шунцу, например 4-5]].
        <div class="hand">
            <span class="closed-part">%%1pin%%2pin%%3pin %%2man%%3man%%4man %%5sou%%6sou%%7sou %%3sou%%3sou %%5man%%6man</span>
        </div>
        Пин-фу стоит 1 хан и собирается исключительно на закрытой руке.
    </div>
    <hr>
    <div class="paragraph">
        Комбинация <strong>иипейко</strong> ([[一盃口|いいぺいこう]]) засчитывается тогда, когда в руке есть два одинаковых [[шунцу|три последовательных тайла масти, например 2-3-4]] одной и той же масти.
        <div class="hand">
            <span class="closed-part">%%1pin%%2pin%%3pin %%1pin%%2pin%%3pin %%5sou%%6sou%%7sou %%3sou%%3sou %%5man%%6man</span>
        </div>
        Как и пин-фу, иипейко стоит 1 хан и собирается исключительно на закрытой руке. Иипейко <strong>не засчитывается</strong>, если в руке есть комбинации чиитойцу или рянпейко.
    </div>
    <hr>
    <div class="paragraph">
        Комбинация <strong>рянпейко</strong> ([[二盃口|りゃんぺいこう]]) засчитывается тогда, когда в руке есть две пары одинаковых [[шунцу|три последовательных тайла масти, например 2-3-4]], каждая пара - одной и той же масти.
        <div class="hand">
            <span class="closed-part">%%1pin%%2pin%%3pin %%1pin%%2pin%%3pin %%5sou%%6sou%%7sou %%5sou%%6sou%%7sou %%5man</span>
        </div>
        Рянпейко стоит 3 хан и собирается исключительно на закрытой руке. Если в руке есть рянпейко, комбинации чиитойцу и иипейко <strong>не засчитываются</strong>.
    </div>
    <hr>
    <div class="paragraph">
        Если в руке есть три одинаковых [[шунцу|три последовательных тайла масти, например 2-3-4]] разных мастей, в руке содержится комбинация <strong>саншоку</strong> ([[三色同順|さんしょくどうじゅん]]).
        <div class="hand">
            <span class="closed-part">%%1pin%%2pin%%3pin %%1sou%%2sou%%3sou %%5sou%%6sou %%east%%east</span>%%1man-rotated%%2man%%3man
        </div>
        Саншоку стоит 2 хан на закрытой руке и 1 хан на открытой.
    </div>
    <hr>
    <div class="paragraph">
        <strong>Иццу</strong> ([[一気通貫|いっきつうかん]]) представляет собой три [[шунцу|три последовательных тайла масти, например 2-3-4]] одной и той же масти, образующие ряд от 1 до 9.
        <div class="hand">
            <span class="closed-part">%%1pin%%2pin%%3pin %%7pin%%8pin%%9pin %%5sou%%6sou %%east%%east</span>%%4pin-rotated%%5pin%%6pin
        </div>
        Иццу стоит 2 хан на закрытой руке и 1 хан на открытой.
    </div>
