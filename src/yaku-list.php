<section id="riichi">
<h3>Риичи</h3>
    <hr>
    <div class="paragraph">
        При темпае на закрытой руке, игрок вправе объявить <strong>риичи</strong> ([[立直|リーチ]]). При этом игрок лишается возможности модифицировать свою руку после объявления.
        В руке может не быть никаких других яку, но риичи как раз дает возможность выиграть на руке, которая изначально не предполагала никакой ценности:
        <div class="hand">
            <span class="closed-part">%%1sou%%1sou %%3sou%%4sou%%5sou %%5man%%7man %%3pin%%3pin%%3pin %%7man%%8man%%9man</span>
        </div>
        Риичи стоит 1 хан.
    </div>
    <hr>
    <div class="paragraph">
        Если риичи объявляется на первом же круге, то он называется <strong>дабл-риичи</strong> ([[ダブル立直|ダブルリーチ]]). Правила те же, что и для обычного риичи, с тем только отличием, что дабл-риичи стоит 2 хан.
    </div>
    <hr>
    <div class="paragraph">
        Если после объявления риичи на первом же круге игрок получает выигрышный тайл со стены или чужого дискарда, ему засчитывается <strong>иппацу</strong> ([[一発|いっぱつ]]).
        Круг не должен быть прерван никакими объявлениями (включая закрытый кан). Иппацу стоит 1 хан.
    </div>
</section>
<section id="wall">
<h3>Яку по взятию со стены</h3>
    <hr>
    <div class="paragraph">
        Если при темпае на закрытой руке игрок получает свой выигрышный тайл со стены, ему засчитывается <strong>мендзен цумо</strong> ([[門前清自摸|めんぜんちんつも]]). Цумо стоит 1 хан.
    </div>
    <hr>
    <div class="paragraph">
        Если игрок побеждает по цумо с последнего тайла стены, ему засчитывается <strong>хайтей</strong> ([[海底摸月|はいていらおゆえ]]). Хайтей стоит 1 хан.
    </div>
    <hr>
    <div class="paragraph">
        Если игрок побеждает по цумо с тайла, взятого с мертвой стены после объявления кана, ему засчитывается <strong>риншан кайхо</strong> ([[嶺上開花|りんしゃんかいほう]]). Риншан стоит 1 хан.
    </div>
    <hr>
    <div class="paragraph">
        Если оя (дилер) побеждает по цумо сразу после начала раздачи (со стартовой руки), то ему засчитывается <strong>тенхо</strong> ([[天和|てんほう]]). Тенхо - <strong>якуман</strong>.
    </div>
    <hr>
    <div class="paragraph">
        Если [[ко|игрок, не являющийся дилером в текущей раздаче]]-игрок побеждает по цумо с первого же взятия со стены, то ему засчитывается <strong>чихо</strong> ([[地和|ちほう]]). До объявления чихо не допускается никаких других объявлений понов, чи и канов, в ином случае чихо не засчитывается. Чихо - <strong>якуман</strong>.
    </div>
