<section id="ponkan">
<h3>Яку на [[коцу|три одинаковых тайла]] / [[канцу|четыре одинаковых тайла, объявленные как сет]]</h3>
    <hr>
    <div class="paragraph">
        Комбинацию <strong>тойтой</strong> ([[対々和|といといほう]]) содержит рука, состоящая только из [[коцу|три одинаковых тайла]] и [[канцу|четыре одинаковых тайла, объявленные как сет]].
        <div class="hand">
            <span class="closed-part">%%west%%west%%west %%2man%%2man %%8sou%%8sou</span>%%5pin-rotated%%akapin%%5pin %%3sou%%3sou%%3sou-rotated
        </div>
        Тойтой стоит 2 хан.
    </div>
    <hr>
    <div class="paragraph">
        Три закрытых [[коцу|три одинаковых тайла]] или [[канцу|четыре одинаковых тайла, объявленные как сет]] сами по себе являются комбинацией <strong>сананко</strong> ([[三暗刻|さんあんこう]]).
        <div class="hand">
            <span class="closed-part">%%west%%west%%west %%2man%%2man%%2man %%8sou%%8sou%%8sou %%chun</span>%%3sou%%3sou%%3sou-rotated
        </div>
        Сананко дает 2 хан. Рука может быть открытой, закрытыми должны быть сами сеты.
        Если рука открыта и в руке ожидание в [[шанпон|ожидание в два пона, например 3-3-7-7, где каждая из двух пар может стать коцу]], сананко засчитывается только по цумо, т.к. при победе по рон, пон считается открытым.
    </div>
    <hr>
    <div class="paragraph">
        Три одинаковых [[коцу|три одинаковых тайла]] или [[канцу|четыре одинаковых тайла, объявленные как сет]] разных мастей называются <strong>саншоку доко</strong> ([[三色同刻|さんしょくどうこう]]).
        <div class="hand">
            <span class="closed-part">%%4man%%4man%%4man %%7sou%%8sou %%west%%west</span>%%4pin-rotated%%4pin%%4pin  %%4sou%%4sou%%4sou-rotated
        </div>
        Саншоку доко всегда дает 2 хан.
    </div>
    <hr>
    <div class="paragraph">
        Три [[канцу|четыре одинаковых тайла, объявленные как сет]] в руке называются <strong>санканцу</strong> ([[三槓子|さんかんつ]]).
        <div class="hand">
            <span class="closed-part">%%1man%%2man %%7sou%%7sou</span>%%closed%%west%%west%%closed %%1sou%%1sou%%1sou-rotated-stacked-upper%%1sou-rotated-stacked-lower %%4pin%%4pin-rotated%%4pin%%4pin
        </div>
        Санканцу дает 2 хан. Каны могут быть как закрытыми, так и открытыми.
    </div>
    <hr>
    <div class="paragraph">
        Четыре [[канцу|четыре одинаковых тайла, объявленные как сет]] в руке называются <strong>сууканцу</strong> ([[四槓子|すうかんつ]]).
        <div class="hand">
            <span class="closed-part">%%1man</span>%%7sou%%7sou-rotated-stacked-upper%%7sou-rotated-stacked-lower%%7sou %%closed%%west%%west%%closed %%1sou%%1sou%%1sou-rotated-stacked-upper%%1sou-rotated-stacked-lower %%4pin%%4pin-rotated%%4pin%%4pin
        </div>
        Сууканцу - <strong>якуман</strong>. Каны могут быть как закрытыми, так и открытыми.
        В случае, если все четыре кана объявил один и тот же игрок, игра продолжается (пересдача не объявляется), но с этого момента игроки не вправе объявлять каны.
    </div>
    <hr>
    <div class="paragraph">
        Четыре закрытых [[коцу|три одинаковых тайла]] или [[канцу|четыре одинаковых тайла, объявленные как сет]] являются комбинацией <strong>сууанко</strong> ([[四暗刻|すうあんこう]]).
        <div class="hand">
            <span class="closed-part">%%west%%west%%west %%2man%%2man%%2man %%8sou%%8sou%%8sou %%3sou%%3sou%%3sou %%chun</span>
        </div>
        Сууанко - <strong>якуман</strong>.
        Если в руке ожидание в [[шанпон|ожидание в два пона, например 3-3-7-7, где каждая из двух пар может стать коцу]], сууанко засчитывается только по цумо, т.к. при победе по рон, пон считается открытым. В случае победы по рон, засчитывается сананко.
    </div>
