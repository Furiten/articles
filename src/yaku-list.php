    </div>
    <hr>
    <div class="paragraph">
        Если в руке содержатся только благородные или терминальные тайлы, рука содержит комбинацию <strong>хонрото</strong> ([[混老頭|ほんろうとう]]). Такое требование исключает возможность существования [[шунцу|три последовательных тайла масти, например 1-2-3]] в руке, поэтому хонрото может сочетаться с одной из двух комбинаций - тойтой или чиитойцу.
        <div class="hand">
            <span class="closed-part">%%west%%west%%west %%1man%%1man %%9sou%%9sou</span>%%haku-rotated%%haku%%haku %%1sou%%1sou%%1sou-rotated
        </div>
        Хонрото стоит 2 хан. Если в руке есть хонрото, комбинация чанта <strong>не засчитывается</strong>.
    </div>
    <hr>
    <div class="paragraph">
        Рука, в каждом из сетов которой есть терминальный тайл, содержит комбинацию <strong>джунчан</strong> ([[純全帯ヤオ|じゅんちゃんたやお]]). Пара также должна быть терминальной. Благородных тайлов в руке быть не должно.
        <div class="hand">
            <span class="closed-part">%%1sou%%1sou %%7sou%%8sou%%9sou %%9man%%9man</span>%%3pin-rotated%%1pin%%2pin %%7man-rotated%%8man%%9man
        </div>
        Джунчан стоит 3 хан на закрытой руке и 2 хан на открытой. В руке должно быть хотя бы одно [[шунцу|три последовательных тайла масти, например 1-2-3]], иначе вместо джунчана засчитывается чинрото.
    </div>
    <hr>
    <div class="paragraph">
        Рука, содержащая джунчан и тойтой, содержит комбинацию <strong>чинрото</strong> ([[清老頭|ちんろうとう]]). Такая рука состоит исключительно из единиц и девяток мастей.
