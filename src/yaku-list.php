<section id="nagashi">
<h2>Нагаши маган</h2>
    <hr>
    Нагаши манган ([[流し満貫|ながしまんがん]]) - особое условие, не относящееся ни к комбинационным, ни к ситуационным яку. Нагаши манган засчитывается в том случае, если дискард игрока состоит исключительно из благородных и терминальных тайлов.
    Из дискарда игрока не должно быть совершено ни одного объявления пона, кана или чи; тем не менее сам игрок может объявлять сеты. Как очевидно из названия, стоимость равна мангану. В правилах стола, принятых в клубе "Фуритен", нагаши манган <strong>отсутствует</strong>.
